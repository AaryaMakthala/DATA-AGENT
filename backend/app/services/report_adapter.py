"""Builds the enriched results sections (redesign brief §1-§20) from the
report dict your `/analyze/{file_id}` route already writes to disk and
`/results/{file_id}` already reads back.

This is intentionally NOT a rewrite of the pipeline. Everything here reads
fields your graph already produces (`profile`, `cleaned_profile`,
`quality_score`, `cleaning_plan`, `recommendations`, `report`, `charts`) and
reshapes them with the Phase 1/2 formatting helpers (app/services/
overview.py, insights.py, quality_presentation.py, ml_presentation.py,
executive_summary.py, cleaning_report.py, before_after.py).

FIXED (this revision), two real bugs from the previous version:

1. Profile semantics were backwards. `data["profile"]` is the ORIGINAL
   pre-cleaning profile -- confirmed from agents/graph.py: `profiler_node`
   sets it once and nothing ever overwrites it. `agents/graph.py` now also
   persists `data["cleaned_profile"]` (post-cleaning, previously computed
   and discarded inside `ml_recommendation_node`). This module now uses
   `profile` as the true "before" and `cleaned_profile` as the true
   "after" -- real before/after cleaning comparisons are now possible
   whenever `cleaned_profile` is present, instead of always being omitted.

2. Chart image URLs were broken. `data["charts"]` holds visualizer.py's
   chart metadata dicts, whose `path` field is an ABSOLUTE FILESYSTEM PATH
   (e.g. `/srv/app/outputs/charts/abc123_bar_Sex.png`), not a URL. The
   previous version of this file passed that raw path straight to the
   frontend as if it were already a `/charts/...` URL --
   `safeResolveAssetUrl()` on the frontend correctly rejected it (doesn't
   match the `/charts/` prefix allowlist), so chart titles rendered but the
   `<img>` never did. Fixed by routing every chart path through
   `file_service.chart_path_to_url` -- the same shared conversion
   `routes.py`'s legacy `_charts_to_urls` uses, so both response shapes are
   guaranteed to agree.

3. FIXED (this revision) -- Bug #1: Overview/banner used the wrong profile.
   This module previously built `ctx` (feeding Overview, the "Upload
   Successful" banner, and Dataset Insights) from `profile_after or
   profile_original` -- i.e. it preferred the CLEANED profile whenever one
   was available. That made the upload banner and Dataset Overview card
   report post-cleaning numbers (e.g. "204 rows" for a Titanic upload that
   actually had 891 rows), directly contradicting the Executive Summary
   (generated by the LLM from the original profile) and the Statistical
   Summary table (built from the original profile's per-column stats) on
   the same page. Both numbers were individually correct -- 204 really is
   the row count after a "drop" missing-value strategy removed every row
   missing the highly-incomplete Cabin column -- but a banner that says
   "Upload Successful! ... 204 rows" for a file that had 891 rows is wrong
   regardless. `ctx` is now always built from `profile_original`. Only two
   things in this module still deliberately use the cleaned profile:
   `quality_source_profile` (quality reflects the data's state *after*
   cleaning, which is correct) and `before_ctx`/`before_after` (the whole
   point of that section is to show original vs. cleaned side by side).

4. ADDED (this revision) -- Bug #3 diagnostics (NOT a fix; logging only).
   A separately reported issue: after a "drop" duplicates strategy runs,
   the final cleaned_profile can still show a nonzero duplicate count,
   which is mathematically impossible for an exact-match dedup. This module
   now logs a comparison of original vs. cleaned duplicate counts and warns
   loudly when that impossible case is observed, without changing any
   duplicate-removal or cleaning logic. A complementary, finer-grained
   diagnostic (row counts immediately before/after the dedup step) was
   added in `app/tools/cleaner.py`'s `clean_csv` -- cross-reference by
   file_id to narrow down whether the problem is in `_apply_duplicates`
   itself or introduced by a later cleaning step.
"""

